'''
+ Adição
- Subtração
* Divisão
/ Divisão
** Potênciação ou pow(3,2)
// Divisão inteira
% Resto da divisão

'''

'''
Ordem de procedência

1° - ()
2° - **
3° - * / // %
4° - + -

'''


print(30 * '*')
print('Soma = {}'.format(5+2))
print('Subtração = {}'.format(5-2))
print('Multiplicação = {}'.format(5*2))
print('Divisão = {}'.format(5/2))
print('Potenciação = {}'.format(5**2))
print('Potenciação = {}'.format(pow(3,2)))
print('Divisão Inteira = {}'.format(5//2))
print('Resto da divisão =  {}'.format(5%2))
print(30 * '*')
